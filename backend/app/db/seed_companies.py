async def seed_companies(db: AsyncSession) -> int:
    """
    Seed the database with popular Indian companies.
    Only inserts companies that don't already exist (by symbol + exchange).
    Returns count of newly inserted companies.
    """
    # Fetch existing symbols in one query
    result = await db.execute(
        select(Company.symbol).where(Company.exchange == "NSE")
    )
    existing_symbols = set(row[0] for row in result.all())

    inserted = 0
    seen = set()  # deduplicate within the seed list itself
    for symbol, name, isin, sector, industry, bse_code in COMPANIES:
        if symbol in existing_symbols or symbol in seen:
            continue
        seen.add(symbol)

        company = Company(
            symbol=symbol,
            exchange="NSE",
            company_name=name,
            isin=isin,
            sector=sector,
            industry=industry,
            bse_code=bse_code,
            nse_symbol=symbol,
            is_active=True,
        )
        db.add(company)
        inserted += 1

    if inserted > 0:
        await db.commit()
        logger.info("Seeded %d new companies into the database (total seed list: %d)", inserted, len(seen))
    else:
        logger.info("Company seed: all companies already present")

    return inserted
